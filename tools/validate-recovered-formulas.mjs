import{calculateMasterUnitEngine,calculateWeaponEngine,calculateAccessoryEngine,calculateCoreDamage,statFormula,roundEven,trunc0,accountRankBP,passiveFlatStats}from'./stat-engine.js';
const failures=[];
const check=(name,actual,expected)=>{if(actual!==expected)failures.push(`${name}: expected ${expected}, got ${actual}`);};
check('trunc0 positive',trunc0(12.9),12);
check('trunc0 negative',trunc0(-12.9),-12);
check('roundEven down',roundEven(10.5),10);
check('roundEven up',roundEven(11.5),12);
check('rank power 300',accountRankBP(300),2000000);
check('level 30 scalar',statFormula({level:30,baseStat:1254}),1254);
check('level 120 Venus HP scalar',statFormula({level:120,baseStat:7700}),25025);
const a=calculateMasterUnitEngine({baseHP:1254,baseATK:264,stars:1,evolvedStars:1,level:30});
check('sample A HP',a.whiteHP,1254);
check('sample A ATK',a.whiteATK,264);
check('sample A power',a.unitPower,15444);
const venusPassives=['LovingOthersPassiveAVenusRegular','BigHeartPassiveAVenusRegular','PlusHPBoostLv3','PlusAttackBoostLv3','GreatAxeMinusDamageLv3','AddWeaponPrefStaff'];
const vp=passiveFlatStats(venusPassives,4);
check('Venus passive HP',vp.hp,1200);
check('Venus passive ATK',vp.atk,300);
const venus=calculateMasterUnitEngine({baseHP:7700,baseATK:1344,stars:6,evolvedStars:6,level:120,extraEvolves:1,refine:2016,awakening:4,boost:148,isAscended:true,mastery:21,passives:venusPassives});
check('Venus HP',venus.whiteHP,84952);
check('Venus ATK',venus.whiteATK,16054);
check('Venus power',venus.unitPower,2190844);
const w=calculateWeaponEngine({baseHP:3995,baseATK:601,stars:6,evolvedStars:6,level:200,extraEvolves:5,refine:10000,awakening:4,boost:300});
check('weapon HP',w.hp,100182);
check('weapon ATK',w.atk,15585);
check('weapon power',w.power,1068642);
const acc=calculateAccessoryEngine({hp:120,atk:20,spd:4});
check('accessory power',acc.power,2040);
check('neutral damage',calculateCoreDamage({attack:375,abilityScalar:3}),1500);
if(failures.length){console.error('Formula validation failed:');for(const f of failures)console.error(`- ${f}`);process.exit(1);}
console.log('Formula validation passed.');
