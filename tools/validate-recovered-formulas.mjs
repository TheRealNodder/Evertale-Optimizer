import{calculateMasterUnitEngine,calculateWeaponEngine,calculateAccessoryEngine,calculateCoreDamage,statFormula,roundEven,trunc0,accountRankBP}from'./stat-engine.js';
const failures=[];
const check=(name,actual,expected)=>{if(actual!==expected)failures.push(`${name}: expected ${expected}, got ${actual}`);};
check('trunc0 positive',trunc0(12.9),12);
check('trunc0 negative',trunc0(-12.9),-12);
check('roundEven down',roundEven(10.5),10);
check('roundEven up',roundEven(11.5),12);
check('rank power 300',accountRankBP(300),2000000);
check('level 30 scalar',statFormula({level:30,baseStat:1254}),1254);
check('level 200 scalar',statFormula({level:200,baseStat:7255}),38088.75);
const a=calculateMasterUnitEngine({baseHP:1254,baseATK:264,stars:1,evolvedStars:1,level:30});
check('sample A HP',a.whiteHP,1254);
check('sample A ATK',a.whiteATK,264);
check('sample A power',a.unitPower,15444);
const b=calculateMasterUnitEngine({baseHP:7255,baseATK:1433,stars:6,evolvedStars:6,level:200,extraEvolves:5,refine:10000,awakening:4,boost:300,isAscended:true,mastery:40});
check('sample B HP',b.whiteHP,277357);
check('sample B ATK',b.whiteATK,57247);
check('sample B power',b.unitPower,8115725);
const w=calculateWeaponEngine({baseHP:3995,baseATK:601,stars:6,evolvedStars:6,level:200,extraEvolves:5,refine:10000,awakening:4,boost:300});
check('sample C weapon HP',w.hp,100182);
check('sample C weapon ATK',w.atk,15585);
check('sample C weapon power',w.power,1068642);
const acc=calculateAccessoryEngine({hp:120,atk:20,spd:4});
check('sample C accessory power',acc.power,2040);
const c=calculateMasterUnitEngine({baseHP:7255,baseATK:1433,stars:6,evolvedStars:6,level:200,extraEvolves:5,refine:10000,awakening:4,boost:300,isAscended:true,mastery:40,gear:{wepHP:w.hp,wepATK:w.atk,accHP:acc.hp,accATK:acc.atk,accSPD:acc.spd}});
check('sample C complete power',c.unitPower,9186407);
check('sample C neutral damage',calculateCoreDamage({attack:375,abilityScalar:3}),1500);
if(failures.length){console.error('Formula validation failed:');for(const f of failures)console.error(`- ${f}`);process.exit(1);}
console.log('Formula validation passed.');
